export class Endereco {
  constructor(
    nome,
    sobrenome,
    rua,
    numero,
    complemento,
    cep,
    bairro,
    cidade,
    estado
  ) {
    this.nome = nome;
    this.sobrenome = sobrenome;
    this.rua = rua;
    this.numero = numero;
    this.complemento = complemento;
    this.cep = cep;
    this.bairro = bairro;
    this.cidade = cidade;
    this.estado = estado;
  }
}
